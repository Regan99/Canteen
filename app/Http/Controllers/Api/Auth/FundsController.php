    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $funds = Funds::find($id);

           $funds->card_id = $input['card_id'];$funds->user_id = $input['user_id'];$funds->fund_amount = $input['fund_amount'];$funds->created_at = $input['created_at'];$funds->updated_at = $input['updated_at'];

