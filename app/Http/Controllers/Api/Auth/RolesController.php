    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $roles = Roles::find($id);

           $roles->role_name = $input['role_name'];$roles->status = $input['status'];$roles->school_id = $input['school_id'];$roles->created_at = $input['created_at'];$roles->updated_at = $input['updated_at'];

            $res = $roles->update();
            if ($res) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $roles
                ], 200);
            }
            return response([
