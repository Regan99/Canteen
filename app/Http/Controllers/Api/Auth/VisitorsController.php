    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $visitors = Visitors::find($id);

           $visitors->card_id = $input['card_id'];$visitors->fund = $input['fund'];$visitors->created_at = $input['created_at'];$visitors->updated_at = $input['updated_at'];

            $res = $visitors->update();
            if ($res) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $visitors
                ], 200);
            }
            return response([
                'status' => 'error',
